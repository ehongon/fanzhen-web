// Helper to create a chapter with auto-generated id
let chapterCounter = 0;
function ch(title: string, content: string[], highlights?: number[], annotations?: Record<number, string>, vernacular?: string[]): Chapter {
  chapterCounter++;
  return { id: `chapter-${chapterCounter}`, title, content, highlights, annotations, vernacular };
}

// Helper to reset counter for each dianji
function resetChapterCounter() {
  chapterCounter = 0;
}

