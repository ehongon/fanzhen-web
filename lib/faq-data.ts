// 获取FAQ统计
export function getFAQStats() {
  const total = faqData.length;
  const byStage = stages.map((stage) => ({
    ...stage,
    count: faqData.filter((f) => f.stage === stage.name).length,
  }));
  const byCategory = categories.map((cat) => ({
    ...cat,
    count: faqData.filter((f) => f.category === cat.name).length,
  }));
  const totalViews = faqData.reduce((sum, f) => sum + f.views, 0);
  const totalHelpful = faqData.reduce((sum, f) => sum + f.helpful, 0);

  return { total, byStage, byCategory, totalViews, totalHelpful };
}

// 搜索FAQ
export function searchFAQ(query: string, filters?: { stage?: string; level?: string; scene?: string; category?: string }) {
  let results = [...faqData];

  if (filters?.stage && filters.stage !== "all") {
    const stageName = stages.find((s) => s.id === filters.stage)?.name;
    if (stageName) results = results.filter((f) => f.stage === stageName);
  }

  if (filters?.level && filters.level !== "all") {
    const levelName = levels.find((l) => l.id === filters.level)?.name;
    if (levelName) results = results.filter((f) => f.level === levelName);
  }

  if (filters?.scene && filters.scene !== "all") {
    const sceneName = scenes.find((s) => s.id === filters.scene)?.name;
    if (sceneName) results = results.filter((f) => f.scene === sceneName);
  }

  if (filters?.category && filters.category !== "all") {
    const catName = categories.find((c) => c.id === filters.category)?.name;
    if (catName) results = results.filter((f) => f.category === catName);
  }

  if (query) {
    const lowerQuery = query.toLowerCase();
    results = results.filter(
      (f) =>
        f.question.toLowerCase().includes(lowerQuery) ||
        f.answer.toLowerCase().includes(lowerQuery) ||
        f.tags.some((t) => t.toLowerCase().includes(lowerQuery))
    );
  }

  return results.sort((a, b) => b.views - a.views);
}
